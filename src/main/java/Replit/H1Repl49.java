package Replit;

public class H1Repl49 {
    public static void main(String[] args) {
        /*Write a java program to print  "Hello Syntax Team! How are you?"
          three times using the while loop
         */

        int i=0;
        while (i<3) {
            System.out.println("Hello Syntax Team! How are you?");
            i++;
        }
    }
}
